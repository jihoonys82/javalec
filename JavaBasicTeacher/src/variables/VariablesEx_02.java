package variables;

public class VariablesEx_02 {
	public static void main(String[] args) {

		// char
		char ch;	//char형 변수 선언
		ch = 'A';	//ch변수에 'A' 값 대입
		System.out.println(ch);
		
		char ch2 = '가';	// 변수의 선언과 동시에 초기화
		System.out.println(ch2);
		
		ch2 = 'B';	// 값 대입
		System.out.println(ch2);
		
//		ch2 = 'AB';// 한 글자만 저장 가능
		
		char ch3 = 65;	// ch3 변수 선언
		ch3 = 'A';
		System.out.println(ch3);
		
	}
}







