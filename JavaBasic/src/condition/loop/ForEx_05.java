package condition.loop;

public class ForEx_05 {

	public static void main(String[] args) {
		// Print "Hello" 7 times
		
		//number of iteration : 7 
		//number range: 0~6

		//initialiZation : int i = 0
		//termination : i<7
		//increment : i++
		
		for(int i=0;i<7;i++) {
			System.out.println("Hello " + i );
		}

	}

}
